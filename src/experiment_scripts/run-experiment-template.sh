#!/bin/bash
#
# Obs: needs to be run as BASH!

EXPERIMENT_NUM=
SCALE=
EPOCHS=300

DDBPN_SEED=
EDSR_SEED=
RCAN_SEED=
RDN_SEED=
WDSR_SEED=


LOG_DIR=logs-${EXPERIMENT_NUM}

function get_date () {
    date +%Y-%m-%d_%Hh%Mm%Ss
}

mkdir -p $LOG_DIR
LOG=$LOG_DIR/log-$(get_date).txt

nvidia-smi -L >> $LOG
echo '' >> $LOG

function run_experiment () {
    echo $@ >> $LOG
    echo "Start time: $(get_date)" >> $LOG 
    start=`python -c 'import time ; print(time.time())'`

    bash experiment_scripts/train-from-params.sh $@ 

    bash experiment_scripts/test-from-params.sh $@ 

    stop=`python -c 'import time ; print(time.time())'`
    echo "End time: $(get_date)" >> $LOG 
    python -c "print('Duration: {:.2f} min\n'.format(($stop-$start)/60))" >> $LOG
}

DIV2K_PARAMS="DATASET=DIV2K_TIFF TRAIN_DATA_RANGE=1-800/1-20 TEST_DATA_RANGE=1-800/1-50 TEST_EVERY=1000"
NAMSS_PARAMS="DATASET=NAMSS TRAIN_DATA_RANGE=1-2746/1-70 TEST_DATA_RANGE=1-2746/1-350 TEST_EVERY=1030"

EDSR_PRE_TRAIN="true"
RDN_PRE_TRAIN="true"
DDBPN_PRE_TRAIN="false"
WDSR_PRE_TRAIN="false"
RCAN_PRE_TRAIN="true"

# EDSR DIV2K
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$EDSR_SEED EPOCHS=$EPOCHS TEMPLATE=EDSR_paper PRE_TRAIN=$EDSR_PRE_TRAIN $DIV2K_PARAMS

# EDSR NAMSS
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$EDSR_SEED EPOCHS=$EPOCHS TEMPLATE=EDSR_paper PRE_TRAIN=$EDSR_PRE_TRAIN $NAMSS_PARAMS

# RDN DIV2K
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$RDN_SEED EPOCHS=$EPOCHS TEMPLATE=RDN PRE_TRAIN=$RDN_PRE_TRAIN $DIV2K_PARAMS

# RDN NAMSS
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$RDN_SEED EPOCHS=$EPOCHS TEMPLATE=RDN PRE_TRAIN=$RDN_PRE_TRAIN $NAMSS_PARAMS

# DDBPN DIV2K
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$DDBPN_SEED EPOCHS=$EPOCHS TEMPLATE=DDBPN PRE_TRAIN=$DDBPN_PRE_TRAIN $DIV2K_PARAMS

# DDBPN NAMSS
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$DDBPN_SEED EPOCHS=$EPOCHS TEMPLATE=DDBPN PRE_TRAIN=$DDBPN_PRE_TRAIN $NAMSS_PARAMS

# WDSR DIV2K
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$WDSR_SEED EPOCHS=$EPOCHS TEMPLATE=WDSR PRE_TRAIN=$WDSR_PRE_TRAIN $DIV2K_PARAMS

# WDSR NAMSS
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$WDSR_SEED EPOCHS=$EPOCHS TEMPLATE=WDSR PRE_TRAIN=$WDSR_PRE_TRAIN $NAMSS_PARAMS

# RCAN DIV2K
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$RCAN_SEED EPOCHS=$EPOCHS TEMPLATE=RCAN PRE_TRAIN=$RCAN_PRE_TRAIN $DIV2K_PARAMS

# RCAN NAMSS
run_experiment EXPERIMENT_NUM=$EXPERIMENT_NUM SCALE=$SCALE SEED=$RCAN_SEED EPOCHS=$EPOCHS TEMPLATE=RCAN PRE_TRAIN=$RCAN_PRE_TRAIN $NAMSS_PARAMS
